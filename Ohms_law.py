# Voltage Calculator using Ohm's Law
# V = I * R

# Get current (in Amperes) from user
current = float(input("Enter the current (in A):5 "))

# Get resistance (in Ohms) from user
resistance = float(input("Enter the resistance (in ohms): 10"))

# Calculate voltage
voltage = current * resistance

# Display result
print(f"The voltage is: 50 {voltage} volts")
